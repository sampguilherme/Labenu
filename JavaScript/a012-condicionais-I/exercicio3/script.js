let nacionalidade = prompt("Qual sua nacionalidade?").toLocaleUpperCase() //= adicione o prompt aqui

if (nacionalidade === "BRASILEIRA") {
    console.log("Você tem a nacionalidade Brasileira")
} else if (nacionalidade === "ARGENTINA") {
    console.log("Você tem a nacionalidade Argentina!")
} else if (nacionalidade === "URUGUAIA") {
    console.log("Você tem a nacionalidade Uruguaia")
} else if (nacionalidade === "CHILENA") {
    console.log("Você tem a nacionalidade Chilea")
} else if (nacionalidade === "COLOMBIANA") {
    console.log("Você tem a nacionalidade Colombiana")
} else {
    console.log("Nacionalidade não")
}